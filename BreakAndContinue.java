package ejemplosloops;




public class BreakAndContinue{
	
	
	public static void main(String[] args) {
		
		
		
		for(int cliente = 0; cliente <= 20; cliente ++){
			
			
			
			if(cliente == 5) {
				
				
				System.out.println("te ganaste un coquita");
				
				continue;
			}

			
			
			System.out.println(cliente);
			
			
		}
		
		
		
		
		
	}
	

	
}